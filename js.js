function fundoVerde(){
    let elemento = document.getElementById("style")

    elemento.style.backgroundColor = "green"
}

function fundoTransparente(){
    let transparente = document.getElementById("style")
    transparente.style.backgroundColor = "transparent"
}

function letraRoxa(){
    let roxo = document.getElementById("style")
    roxo.style.color = "purple"
}

function removeRoxo(){
    let removeroxo = document.getElementById("style")
    removeroxo.style.color = "black"
}